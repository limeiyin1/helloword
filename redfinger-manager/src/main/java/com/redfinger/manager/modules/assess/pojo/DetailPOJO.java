package com.redfinger.manager.modules.assess.pojo;

public class DetailPOJO {
	private String optionName;
	private String optionPoint;
	private String assessAdmin;
	private String point;

	public String getOptionName() {
		return optionName;
	}

	public void setOptionName(String optionName) {
		this.optionName = optionName;
	}

	public String getOptionPoint() {
		return optionPoint;
	}

	public void setOptionPoint(String optionPoint) {
		this.optionPoint = optionPoint;
	}

	public String getAssessAdmin() {
		return assessAdmin;
	}

	public void setAssessAdmin(String assessAdmin) {
		this.assessAdmin = assessAdmin;
	}

	public String getPoint() {
		return point;
	}

	public void setPoint(String point) {
		this.point = point;
	}

}
